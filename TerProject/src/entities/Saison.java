package entities;

public class Saison {

	private int idSaison;
	private int idSerie;
	private String libelleSaison;
	private int nbEpisode;
	
	
	
	public int getIdSaison() {
		return idSaison;
	}
	public void setIdSaison(int idSaison) {
		this.idSaison = idSaison;
	}
	public int getIdSerie() {
		return idSerie;
	}
	public void setIdSerie(int idSerie) {
		this.idSerie = idSerie;
	}
	public String getLibelleSaison() {
		return libelleSaison;
	}
	public void setLibelleSaison(String libelleSaison) {
		this.libelleSaison = libelleSaison;
	}
	public int getNbEpisode() {
		return nbEpisode;
	}
	public void setNbEpisode(int nbEpisode) {
		this.nbEpisode = nbEpisode;
	}
	
	
}
